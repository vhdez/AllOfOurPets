public class Person {

    int age;
    String name;
    String occupation;
    String ethnicity;
    String gender;

    String height;

    public Person() {
    }
    public Person(int age, String name, String occupation,  String ethnicity, String gender,  String height) {
        this.age = age;
        this.occupation = occupation;
        this.age = age;
        this.ethnicity = ethnicity;
        this.name = name;
        this.gender = gender;
        this.height = height;
    }


}
